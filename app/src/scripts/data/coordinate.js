/** @class Coordinate describing a coordinate in 2D-space. */
export default class Coordinate {
  /**
   * Instantiates a Coordinate.
   * 
   * @constructor
   * @param {number} x The x attribute of the coordinate.
   * @param {number} y The y attribute of the coordinate.
   */
  constructor(x, y) {
    this.x = x;
    this.y = y;
  }
}
